# Файл: python/warmup-2/string_bits.py

# --- Условие ---
# Given a string, return a new string made of every other char starting
# with the first, so "Hello" yields "Hlo".
#
# string_bits('Hello') → 'Hlo'
# string_bits('Hi') → 'H'

# --- Мой анализ ---
# 1. Понимание: Нужно извлечь из строки
# символы с четными индексами (0, 2, 4...).
# 2. Крайние случаи: Пустая строка "" -> "".
#    Строка из одного символа "H" -> "H".
#    Срез `[::2]` в Python корректно обрабатывает оба случая.
# 3. Сложность:
#    - Временная: O(N), N - длина строки.
#    - Пространственная: O(N).
# 4. Альтернативы: Можно было бы использовать цикл for и
#    проверку `if index % 2 == 0`,
#    но срез с шагом - это самый эффективный и идиоматичный способ.

# --- Решение ---


def string_bits(str):
    return str[::2]
