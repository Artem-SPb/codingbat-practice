# Файл: python/warmup-2/array_count9.py

# --- Условие ---
# Given an array of ints, return the number of 9's in the array.
#
# array_count9([1, 2, 9]) → 1

# --- Мой анализ ---
# 1. Понимание: Нужно посчитать вхождения числа 9 в списке.
# 2. Крайние случаи: Пустой список -> 0. Список без девяток -> 0.
#    Метод .count() корректно обрабатывает оба случая.
# 3. Сложность:
#    - Временная: O(N), где N - количество элементов в списке.
#    - Пространственная: O(1).
# 4. Альтернативы: Ручной подсчет с помощью цикла for, но это избыточно,
#    когда есть встроенный метод.

# --- Решение ---


def array_count9(nums):
    return nums.count(9)
