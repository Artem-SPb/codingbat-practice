# Файл: python/warmup-2/string_splosion.py

# --- Условие ---
# Given a non-empty string like "Code" return a string like "CCoCodCode".
#
# string_splosion('Code') → 'CCoCodCode'
# string_splosion('abc') → 'aababc'

# --- Мой анализ ---
# 1. Понимание: Нужно итеративно наращивать строку, добавляя на каждом
#    шаге префикс исходной строки увеличивающейся длины.
# 2. Крайние случаи: Строка из одного символа 'a' -> 'a'. Цикл
#    сработает для i=1, возьмет срез str[:1] ('a') и вернет его. Корректно.
# 3. Сложность:
#    - Временная: O(N^2) из-за неэффективности строковой конкатенации в цикле.
#    - Пространственная: O(N^2) для хранения результирующей строки.
# 4. Альтернативы: Более эффективный способ - сначала собрать все подстроки
#    в список, а затем соединить их методом `''.join()`:
#    `return ''.join([str[:i] for i in range(1, len(str) + 1)])`
#    Это снижает временную сложность до O(N^2) более стабильно.

# --- Решение ---


def string_splosion(str):
    result = ''
    for i in range(1, len(str) + 1):
        result = result + str[:i]
    return result
