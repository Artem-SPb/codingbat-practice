# Файл: python/warmup-1/makes10.py

# --- Условие ---
# Given 2 ints, a and b,
# return True if one if them is 10 or if their sum is 10.
#
# makes10(9, 10) → True
# makes10(9, 9) → False
# makes10(1, 9) → True

# --- Мой анализ ---
# 1. Понимание: Вход - два целых числа.
#    Нужно проверить три условия через "ИЛИ":
#    a == 10, b == 10, a + b == 10. Если хотя бы одно истинно, результат True.
# 2. Крайние случаи: Отрицательные числа, нули. Алгоритм
#    корректно их обработает. Например, makes10(-5, 15) -> True.
# 3. Сложность:
#    - Временная: O(1).
#    - Пространственная: O(1).
# 4. Альтернативы: Нет более простого или чистого способа, чем прямое
#    возвращение результата логического выражения.

# --- Решение ---


def makes10(a, b):
    return a == 10 or b == 10 or a + b == 10
