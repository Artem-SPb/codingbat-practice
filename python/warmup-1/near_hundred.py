# Файл: python/warmup-1/near_hundred.py

# --- Условие ---
# Given an int n, return True if it is within 10 of 100 or 200.
# Note: abs(num) computes the absolute value of a number.
#
# near_hundred(93) → True
# near_hundred(90) → True
# near_hundred(89) → False

# --- Мой анализ ---
# 1. Понимание: Вход - целое число n. Нужно проверить, попадает ли оно
#    в один из двух диапазонов: [90, 110] или [190, 210].
# 2. Крайние случаи: 90, 110, 190, 210. Алгоритм должен включать
#    эти значения.
# 3. Сложность:
#    - Временная: O(1).
#    - Пространственная: O(1).
# 4. Альтернативы: Можно было бы использовать прямое сравнение с диапазонами:
#    (n >= 90 and n <= 110) or (n >= 190 and n <= 210).
#    Решение с abs() более точно следует формулировке "в пределах 10 от...".

# --- Решение ---


def near_hundred(n):
    # Проверяем, что абсолютная разница с 100 не больше 10
    is_near_100 = abs(100 - n) <= 10
    # Проверяем, что абсолютная разница с 200 не больше 10
    is_near_200 = abs(200 - n) <= 10
    return is_near_100 or is_near_200
