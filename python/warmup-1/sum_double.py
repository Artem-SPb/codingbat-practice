# --- Условие ---
# Given two int values, a and b, return their sum. Unless the values are the
# same, then return double their sum.
#
# sum_double(1, 2) → 3
# sum_double(3, 3) → 12
# sum_double(2, 2) → 8

# --- Мой анализ ---
# 1. Понимание: Вход - два целых числа. Если они равны, вернуть
#    удвоенную сумму. Иначе - просто сумму.
# 2. Крайние случаи: 0, отрицательные числа. Алгоритм должен
#    корректно работать и с ними.
# 3. Сложность:
#    - Временная: O(1) - количество операций постоянно.
#    - Пространственная: O(1) - дополнительная память не используется.
# 4. Альтернативы: Можно использовать тернарный оператор для более
#    компактной записи: `return (a + b) * 2 if a == b else a + b`.

# --- Решение ---


def sum_double(a, b):
    sum_val = a + b
    if a == b:
        sum_val = sum_val * 2
    return sum_val
