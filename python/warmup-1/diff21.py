# --- Условие ---
# Given an int n, return the absolute difference between n and 21,
# except return double the absolute difference if n is over 21.
#
# diff21(19) → 2
# diff21(10) → 11
# diff21(21) → 0

# --- Мой анализ ---
# 1. Понимание: Вход - одно целое число n.
#    - Если n > 21, вернуть (n - 21) * 2.
#    - Если n <= 21, вернуть 21 - n.
# 2. Крайние случаи: n = 21 (вернет 0), n = 22 (вернет 2).
#    Логика корректно обрабатывает эти пограничные случаи.
# 3. Сложность:
#    - Временная: O(1).
#    - Пространственная: O(1).
# 4. Альтернативы: Можно было бы использовать abs(), но это не упростит
#    код, так как логика все равно разделяется на два случая.
#    Простой if/else - самый чистый вариант.

# --- Решение ---
def diff21(n):
    if n > 21:
        return (n - 21) * 2
    else:
        return 21 - n
