from typing import List


class Solution:
    def coinChange(self, coins: List[int], amount: int) -> int:
        """
        Ищет минимальное количество монет для сбора суммы.
        Использует паттерн Динамического программирования (Bottom-Up).

        Сложность по времени: O(amount * len(coins))
        Сложность по памяти: O(amount)
        """
        # Заполняем массив dp значениями бесконечности.
        # Размер amount + 1 нужен, чтобы индекс i совпадал с искомой суммой.
        dp = [float('inf')] * (amount + 1)

        # Базовый случай: для суммы 0 нужно 0 монет
        dp[0] = 0

        # Вычисляем ответ для каждой суммы от 1 до amount
        for i in range(1, amount + 1):
            for coin in coins:
                # Если монету можно использовать, не уйдя в минус
                if i - coin >= 0:
                    # Выбираем лучшее из того,
                    # что уже было найдено для этой суммы,
                    # ИЛИ берем решение для
                    # (сумма - текущая монета) + 1 (сама эта монета)
                    dp[i] = min(dp[i], dp[i - coin] + 1)

        # Если значение не изменилось с бесконечности, сумму собрать нельзя
        return dp[amount] if dp[amount] != float('inf') else -1
