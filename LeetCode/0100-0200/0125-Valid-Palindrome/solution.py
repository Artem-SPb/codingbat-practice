class Solution:
    def isPalindrome(self, s: str) -> bool:
        """
        Проверяет, является ли строка палиндромом, используя
        паттерн "Два указателя".

        Сложность по времени: O(n), где n - длина строки.
        Сложность по памяти: O(1), если не считать создание новой строки,
                             O(n) - если считать.
        """
        # Шаг 1: Предобработка строки (альтернативный, более простой подход)
        # new_s = ""
        # for char in s:
        # isalnum() проверяет, является ли символ буквой или цифрой
        #     if char.isalnum():
        #         new_s += char.lower()

        # --- Основной алгоритм с двумя указателями ---
        # l, r = 0, len(new_s) - 1
        # while l < r:
        #     if new_s[l] != new_s[r]:
        #         return False
        #     l += 1
        #     r -= 1
        # return True

        # Более оптимальное решение без создания новой строки (O(1) по памяти)
        l, r = 0, len(s) - 1

        while l < r:
            # Двигаем левый указатель, пока не найдем букву или цифру
            while l < r and not s[l].isalnum():
                l += 1

            # Двигаем правый указатель, пока не найдем букву или цифру
            while l < r and not s[r].isalnum():
                r -= 1

            # Сравниваем символы в нижнем регистре
            if s[l].lower() != s[r].lower():
                return False

            # Сдвигаем указатели навстречу друг другу
            l += 1
            r -= 1

        return True
