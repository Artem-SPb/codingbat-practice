from typing import List


class Solution:
    def findMin(self, nums: List[int]) -> int:
        """
        Находит минимальный элемент в повернутом отсортированном массиве.
        Использует бинарный поиск для поиска точки излома (аномалии).

        Сложность по времени: O(log N)
        Сложность по памяти: O(1)
        """
        left, right = 0, len(nums) - 1

        # Используем left < right, чтобы выйти из цикла,
        # когда указатели встретятся.
        while left < right:
            mid = (left + right) // 2

            # Если середина больше правого края, значит точка излома (минимум)
            # находится строго правее середины.
            if nums[mid] > nums[right]:
                left = mid + 1
            # Иначе точка излома находится левее или это и есть сама середина.
            else:
                right = mid

        # В конце left и right указывают на один и тот же минимальный элемент
        return nums[left]


# --- Блок самопроверки ---
if __name__ == "__main__":
    solution = Solution()

    # Тест 1: Излом ближе к концу
    assert solution.findMin([3, 4, 5, 1, 2]) == 1, "Ошибка в тесте 1"

    # Тест 2: Излом ближе к началу
    assert solution.findMin([4, 5, 6, 7, 0, 1, 2]) == 0, "Ошибка в тесте 2"

    # Тест 3: Массив вообще не повернут (идеально отсортирован)
    assert solution.findMin([11, 13, 15, 17]) == 11, "Ошибка в тесте 3"

    # Тест 4: Всего два элемента
    assert solution.findMin([2, 1]) == 1, "Ошибка в тесте 4"

    print("Все тесты пройдены успешно!")
