from typing import List


class Solution:
    def findPeakElement(self, nums: List[int]) -> int:
        """
        Находит индекс любого пикового элемента в неотсортированном массиве.
        Использует бинарный поиск,
        опираясь на направление "склона" (рост/падение значений).

        Сложность по времени: O(log N)
        Сложность по памяти: O(1)
        """
        left, right = 0, len(nums) - 1

        while left < right:
            mid = (left + right) // 2

            # Если график растет (текущий элемент меньше следующего)
            if nums[mid] < nums[mid + 1]:
                # Пик гарантированно где-то справа
                left = mid + 1
            else:
                # Если график падает,
                # пик находится слева (или это и есть текущий элемент)
                right = mid

        # Указатели встретятся на пиковом элементе
        return left


# --- Блок самопроверки ---
if __name__ == "__main__":
    solution = Solution()

    # Тест 1: Единственный пик в середине
    assert solution.findPeakElement([1, 2, 3, 1]) == 2, "Ошибка в тесте 1"

    # Тест 2: Множество пиков
    # (алгоритм найдет индекс 5, так как он пойдет вправо от первой середины)
    assert solution.findPeakElement(
        ([1, 2, 1, 3, 5, 6, 4]) in [1, 5], "Ошибка в тесте 2")

    # Тест 3: Строго возрастающий массив (пик в самом конце)
    assert solution.findPeakElement([1, 2, 3, 4, 5]) == 4, "Ошибка в тесте 3"

    # Тест 4: Строго убывающий массив (пик в самом начале)
    assert solution.findPeakElement([5, 4, 3, 2, 1]) == 0, "Ошибка в тесте 4"

    # Тест 5: Массив из одного элемента
    assert solution.findPeakElement([1]) == 0, "Ошибка в тесте 5"

    print("Все тесты пройдены успешно!")
