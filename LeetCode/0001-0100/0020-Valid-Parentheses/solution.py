class Solution:
    def isValid(self, s: str) -> bool:
        """
        Проверяет, является ли строка со скобками сбалансированной,
        используя стек.

        Сложность по времени: O(n), где n - длина строки.
        Сложность по памяти: O(n) в худшем случае для стека.
        """
        # Стек для хранения открывающих скобок
        stack = []

        # Словарь для быстрого сопоставления закрывающих скобок с открывающими
        mapping = {")": "(", "}": "{", "]": "["}

        for char in s:
            # Если символ - закрывающая скобка
            if char in mapping:
                # Проверяем, не пуст ли стек. Если пуст, то закрывать нечего.
                # Если верхний элемент стека - не соответствующая
                # открывающая, то ошибка.
                if not stack or stack.pop() != mapping[char]:
                    return False
            # Если символ - открывающая скобка
            else:
                stack.append(char)

        # Если после прохода по всей строке стек пуст, значит все скобки
        # были правильно закрыты. Если в нем что-то осталось - нет.
        return not stack
