import heapq
from typing import List


class Solution:
    def findKthLargest(self, nums: List[int], k: int) -> int:
        """
        Находит k-й наибольший элемент в массиве с использованием min-heap.

        Сложность по времени: O(N log k), где N - количество элементов в nums.
        Сложность по памяти: O(k) для хранения кучи.
        """

        # Создаем min-heap. Мы будем поддерживать в ней ровно k самых
        # больших элементов, которые мы встретили.
        min_heap = []

        for num in nums:
            # Добавляем текущий элемент в кучу.
            heapq.heappush(min_heap, num)

            # Если размер кучи превысил k, это значит, что в ней находится
            # k+1 элементов. Нам нужно удалить самый маленький из них,
            # чтобы в куче снова остались только k самых больших.
            # В min-heap самый маленький элемент - это всегда корень.
            if len(min_heap) > k:
                heapq.heappop(min_heap)

        # После прохода по всем элементам, в куче осталось ровно k
        # самых больших чисел. Корень этой min-heap (самый маленький
        # элемент среди них) и есть k-й наибольший элемент.
        return min_heap[0]
