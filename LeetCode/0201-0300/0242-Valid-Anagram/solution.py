class Solution:
    def isAnagram(self, s: str, t: str) -> bool:
        """
        Проверяет, являются ли строки s и t анаграммами, используя
        хэш-таблицу для подсчета частот символов.

        Сложность по времени: O(n), где n - длина строк.
        Сложность по памяти: O(1), т.к. используется не более 26 ключей
                                для английского алфавита.
        """
        # 1. Ранний выход: если длины строк разные,
        # они не могут быть анаграммами.
        if len(s) != len(t):
            return False

        # 2. Создаем словарь для подсчета частоты символов в строке s.
        s_counts = {}
        for char in s:
            s_counts[char] = s_counts.get(char, 0) + 1

        # 3. Проходим по строке t и "вычитаем" символы из нашего словаря.
        for char in t:
            # Если символа из t нет в словаре или его счетчик уже 0,
            # значит строки не совпадают.
            if char not in s_counts or s_counts[char] == 0:
                return False
            s_counts[char] -= 1

        # 4. Если мы успешно прошли весь цикл, значит строки - анаграммы.
        return True
